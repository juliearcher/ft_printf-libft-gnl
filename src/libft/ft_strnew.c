#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	if ((str = (char *)malloc(size + 1)) == 0)
		return (0);
	i = -1;
	while (++i <= size)
		str[i] = 0;
	return (str);
}
