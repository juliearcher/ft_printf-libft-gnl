char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	j;

	i = 0;
	while (s1[i] != 0)
		i = i + 1;
	j = 0;
	while (s2[j] != 0)
	{
		s1[i] = s2[j];
		i = i + 1;
		j = j + 1;
	}
	s1[i] = 0;
	return (s1);
}
