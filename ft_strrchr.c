const char	*ft_strrchr(const char *s, int c)
{
	const char	*ret;
	int			i;

	ret = 0;
	i = -1;
	while (s != 0 && s[++i] != 0)
		if (s[i] == c)
			ret = &s[i];
	if (s[i] == c)
		ret = &s[i];
	return (ret);
}
